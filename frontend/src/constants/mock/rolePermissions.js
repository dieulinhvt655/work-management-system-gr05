export function getPermissionsForMockRole(mockRole) {
  return ROLE_PERMISSIONS[mockRole] ?? []
}

export function createMockUser(mockRole, emailOverride) {
  const profile = MOCK_USERS[mockRole]
  if (!profile) {
    throw new Error(`Unknown mock role: ${mockRole}`)
  }

  const isSystemAdmin = mockRole === MOCK_ROLES.SYSTEM_ADMIN

  return {
    id: `mock-${mockRole.toLowerCase()}`,
    email: emailOverride || profile.email,
    fullName: profile.fullName,
    permissions: getPermissionsForMockRole(mockRole),
    // Scope: Admin operates system-wide; Owner is bound to managed workspace(s).
    workspaceId: isSystemAdmin ? null : 'ws-demo-001',
    managedWorkspaceIds: isSystemAdmin ? [] : ['ws-demo-001'],
  }
}
